void draw() {
	glLoadIdentity();
	gluOrtho2D(0, 10, 0, 10);
	glClear(GL_COLOR_BUFFER_BIT);

	/*active opacity*/
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	glEnable(GL_BLEND);
	//

	background();

	glPushMatrix();
	glTranslated(4, 1, 0);
	glScaled(1, 0.4, 0);
	glRotated(45, 0, 0, 0);
	shadow();
	glPopMatrix();

	shadow1();

	glPushMatrix();
	glTranslated(-7.1, -0.5, 0);
	glScaled(1, 1, 0);
	moonlight();
	glPopMatrix();

	glPushMatrix();
	glTranslated(-8.5, -8.3, 0);
	glScaled(2, 2, 0);
	light();
	glPopMatrix();

	head();
	pants();
	body();
	hatfront();
	arms();
	light();
	bright();
	beltandboots();
	belt();
	glFlush();

}


int main(int argc, char* argv[]) {
	srand(time(NULL));
	glutInit(&argc, argv);
	glutInitDisplayMode(GLUT_SINGLE);
	glutInitWindowSize(800, 800);
	glutInitWindowPosition(50, 50);
	glutCreateWindow("BlackMage");
	glutDisplayFunc(draw);
	glutMainLoop();
	return 0;
}
